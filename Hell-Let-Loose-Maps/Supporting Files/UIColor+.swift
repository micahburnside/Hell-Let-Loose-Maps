import Foundation
import UIKit

extension UIColor{
    class func getFriendlyBlue() -> UIColor{
        return UIColor(red:67, green:150 ,blue:206 , alpha:1.00)
    }
    class func getEnemyRed() -> UIColor{
        return UIColor(red:67, green:150 ,blue:206 , alpha:14)
    }
    class func getNeutralWhite() -> UIColor{
        return UIColor(red: 255, green: 255, blue: 255, alpha: 28)
    }
}
